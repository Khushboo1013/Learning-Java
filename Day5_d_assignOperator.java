public class Day5_d_assignOperator {
    public static void main(String[] args) {
        /*
          Assignment Operator
           
          =        a=b;
          +=       a=a+10(a+=10); 
          -=       B=B-5(B-=5);
          *=
          /=
        */
        int A=20;
        A+=45;  //A=A+45;
        int B=10;
        B-=5;  //B=B-5;
        System.out.println(A);
        System.out.println(B);
    }
}
