public class Day5_b_RelatOperator {
    public static void main(String[] args) {
        /*
           Relational Operators(check relation)
           ==  equal to
           !=  not equal to
           >   greater than
           <   less than
           >=  greater than equal to
           <=  smaller than equal to
        */
        int a =10;
        int b=2;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>=b);
        System.out.println(a>b);
        System.out.println(a<=b);
        System.out.println(a<b);
    }
}
