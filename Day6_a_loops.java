public class Day6_a_loops {
    public static void main(String args[]){
        /*
         loop
         for loop
         while loop
         
         while(condition){
            //do something
         }
         Do while
        */

        // Print hello world 10 times by using while loop
        int counter=0;
        while(counter<10){
            System.out.println("Hello world");
            counter++;
        }
        System.out.println("I am back");
    }
}
